package getters_and_setters;

public class Taco {
	String sauce;
	String meat;

	public String getSauce() {
		return sauce;
	}

	public String getMeat() {
		return meat;

	}

	public void setMeat(String meat) {
		this.meat = meat;

	}

	public void setSauce(String sauce) {
		this.sauce = sauce;

	}
}
